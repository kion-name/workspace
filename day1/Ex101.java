package day1;

public class Ex101 {
	public static void main(String[] args) {
		int num; // 변수의 선언
		num = 5; // 변수의 초기화(= --> "대입연산자" --> "오른쪽을 정리해서 왼쪽에 담는다")
		System.out.println("num :"+num); // 문자열 + (무언가) = 문자
		System.out.println("num+num :"+num+num); // 이해
		
		int num3;
		//System.out.println("num3 : "+num3); // 변수의 초기화가 없어서 사용 불가
		// num4 = 5.15 // 변수의 선언이 없고, 실수형 자료형도 아니기때문에 불가능
		
		int a = 10;
		double b = 11.1;
		System.out.println("a = "+ a);
		System.out.println("b = "+ b);
		
		
	}

}
