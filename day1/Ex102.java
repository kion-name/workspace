package day1;
// 정수          int
// 실수	   double
// 문자열       String
// 문자 ->   char ch = 'A';

public class Ex102 {
	public static void main(String[] args) {
		int n =5;
		System.out.println(n);
		double n2 = 5.15;
		System.out.println(n2);
		String s = "ABC"; // 따옴표("") 안에는 0개 이상의 글
		System.out.println(s);
		char ch = 'A'; 	  // 홀따움표('') 안에는 딱 1글
		System.out.println(ch);
		
	}
}
